    /* ========================================================================================== */
    //                                    Data structures
    /* ========================================================================================== */
//    A data structure is a way of organizing and storing data in a computer so that it can be accessed and modified efficiently.
//    Different types of data structures are suited to different kinds of applications, and some are highly specialized to specific tasks.

/*
    Some common data structures include:
    Arrays: An array is a simple data structure that stores a fixed-size sequential collection of elements of the same type.
    Lists: A list is a data structure that stores a collection of items, where each item has a position within the list. Lists can be used to store items of any data type and can be of variable size.
    Stacks: A stack is a data structure that stores a collection of items and allows only the last item added to be removed (last-in, first-out) - LIFO.
    Queues: A queue is a data structure that stores a collection of items and allows only the first item added to be removed (first-in, first-out) - FIFO.
    Trees: A tree is a data structure that stores a collection of items in a hierarchical structure, with a root node and child nodes.
    Graphs: A graph is a data structure that consists of a set of vertices (or nodes) and a set of edges connecting these vertices.
    Hash tables: A hash table is a data structure that uses a hash function to map keys to indices in an array, allowing for fast insertion and retrieval of data.
*/

//    Each of these data structures has its own characteristics and trade-offs, and choosing the right data structure for a particular task can be crucial to the efficiency and performance of a software application.

    /* ========================================================================================== */
    //                                    Collections Framework
    /* ========================================================================================== */
/*
    The Java Collections Framework is a set of classes and interfaces that provide commonly used data structures in Java.
    These data structures include lists, maps, sets, and queues, and are designed to be used in a way that is both easy to understand and efficient.

    The Collections Framework provides a set of interfaces that define the various types of data structures,
    such as the List interface for lists and the Map interface for maps.
    It also provides a set of classes that implement these interfaces, such as ArrayList and HashMap.

    The use of the Collections Framework can greatly simplify the process of working with data structures in Java,
    as it provides a standard set of classes and interfaces that can be used in a consistent manner across different applications.
    It also helps to improve the efficiency of your code by providing optimized implementations of common data structures and algorithms.
*/
